/**
 * @swagger
 * /auth/user-by-email/{email}:
 *   get:
 *     summary: Get user by email
 *     tags: [Users]
 *     parameters:
 *       - in: path
 *         name: email
 *         required: true
 *         schema:
 *           type: string
 *         description: The user's email address
 *     responses:
 *       200:
 *         description: The user data
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 name:
 *                   type: string
 *                 email:
 *                   type: string
 *                 profilePic:
 *                   type: string
 *       400:
 *         description: Email is required
 *       404:
 *         description: User not found
 *       500:
 *         description: Server error while fetching user
 */
router.get(
  "/user-by-email/:email",
  async (req: Request, res: Response): Promise<void> => {
    try {
      const email = decodeURIComponent(req.params.email); // âœ… Decode email
      if (!email) {
        res.status(400).json({ message: "Email is required" });
        return;
      }

      const user = await userModel.findOne({ email });
      if (!user) {
        res.status(404).json({ message: "User not found" });
        return;
      }

      res.status(200).json({
        name: user.name,
        email: user.email,
        profilePic: user.imgUrl || "", // âœ… Ensure correct property
      });
    } catch (error) {
      console.error("ðŸ›‘ Error fetching user by email:", error);
      res.status(500).json({ message: "Server error while fetching user" });
    }
  }
);

